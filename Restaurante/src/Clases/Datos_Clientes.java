    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        //GEN-FIRST:event_jButton4ActionPerformed
        try {
            int i = metodo.eliminar_cliente(Integer.valueOf(jLabel10.getText()));

            if (i > 0) {
                metodo.eliminar_persona(Integer.valueOf(jLabel9.getText()));
                JOptionPane.showMessageDialog(this, 
                            "Datos Eliminados Correctamente");

                metodos_cargarcombobox.clave_cliente(jLabel10);
                metodos_cargarcombobox.clave_persona(jLabel9);
                con.conectar();
                limpiar();
                mostrartabla();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar los datos");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed


