    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        //GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jButton7.disable();
        try {
            int i = metodo.actualizar_persona(jTextField3.getText(), 
            jPasswordField1.getText(), jTextField4.getText(), 
            jTextField5.getText(),  jTextField6.getText(), 
            jTextField7.getText(), jTextField8.getText(), 
            Integer.valueOf(jLabel9.getText()));
            if (i > 0) {

                JOptionPane.showMessageDialog(this, 
                "Datos Actualizados Correctamente");

                metodos_cargarcombobox.clave_cliente(jLabel10);
                metodos_cargarcombobox.clave_persona(jLabel9);
                con.conectar();
                mostrartabla();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, 
                "No se pudo Actualizar los datos");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {
        //GEN-FIRST:event_jButton7ActionPerformed
        if (String.valueOf(jPasswordField1.getPassword()).
        equals(String.valueOf(jPasswordField2.getPassword()))) {
            try {
                int i = metodo.guardar_persona(Integer.valueOf(
                    jLabel9.getText()), jTextField1.getText(),
                     jPasswordField1.getText(), jTextField2.getText(), 
                     jTextField3.getText(), jTextField4.getText(),
                      jTextField5.getText(), jTextField6.getText());

                if (i > 0) {
                    metodo.guardar_cliente(Integer.valueOf(jLabel10.getText()),
                     Integer.valueOf(jLabel9.getText()));
                    JOptionPane.showMessageDialog(this, 
                    "Datos Guardados Correctamente");
                    con.conectar();
                    limpiar();
                    metodos_cargarcombobox.clave_cliente(jLabel10);
                    metodos_cargarcombobox.clave_persona(jLabel9);
                    mostrartabla();
                } else {
                    JOptionPane.showMessageDialog(this,
                     "No se pudo guardar los datos");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Registro.class.getName()).
                log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null,
             "Contraseña Incorrecta");
        }
    }//GEN-LAST:event_jButton7ActionPerformed


