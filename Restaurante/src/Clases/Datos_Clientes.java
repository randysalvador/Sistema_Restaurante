    void mostrartabla() throws SQLException {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id_Cliente");
        modelo.addColumn("Id_Persona");
        modelo.addColumn("Usuario");
        modelo.addColumn("Contraseña");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido Paterno");
        modelo.addColumn("Apedllido Materno");
        modelo.addColumn("Dirección");
        modelo.addColumn("Teléfono");
        tabladatos.setModel(modelo);

        String sql = "SELECT C.ID_CLIENTE AS N_CLIENTE,P.ID_PERSONA AS CLAVE_P, "
                + "P.USUARIO AS USUARIO,P.CONTRASENA AS CONTRASENA, P.NOMBRE AS "
                + "NOMBRE,P.APELLIDO_P AS APELLIDO_PATERNO, P.APELLIDO_M AS "
                + "APELLIDO_MATERNO, P.DIRECCION AS DIRECCION, P.TELEFONO AS TELEFONO\n"
                + "FROM PERSONA AS P, CLIENTES AS C\n"
                + "WHERE P.ID_PERSONA=C.ID_PERSONA";
        String datos[] = new String[9];
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                modelo.addRow(datos);
            }
            tabladatos.setModel(modelo);
        } catch (Exception ex) {
            Logger.getLogger(Datos_Clientes.class.getName())
            .log(Level.SEVERE, null, ex);
        }
    }


