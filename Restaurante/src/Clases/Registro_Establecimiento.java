    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        //GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            try {
                int i = metodo.guardar_establecimiento(
                    Integer.valueOf(jLabel5.getText()),
                    jTextField2.getText(),jTextField3.getText(),
                    jTextField4.getText());
                
                if (i>0){
                    JOptionPane.showMessageDialog(this, 
                    "Datos Guardados Correctamente");
                    
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    metodos_cargarcombobox.clave_Id_Establecimiento(jLabel5);
                    mostrartabla();
                }else{
                    JOptionPane.showMessageDialog(this,
                     "No se pudo guardar los datos");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Registro_Establecimiento.class.
                getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

